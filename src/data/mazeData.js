export const ROWS = MAZE_LAYOUT.length;
export const COLS = MAZE_LAYOUT[0].length;

export function isWalkable(x, z) {
    const col = Math.floor(x);
    const row = Math.floor(z);
    if (row < 0 || row >= ROWS || col < 0 || col >= COLS) return false;
    const cell = MAZE_LAYOUT[row][col];
    return cell !== CELL.WALL;
}

export function isWalkableForGhost(x, z) {
    const col = Math.floor(x);
    const row = Math.floor(z);
    if (row < 0 || row >= ROWS || col < 0 || col >= COLS) return false;
    const cell = MAZE_LAYOUT[row][col];
    return cell !== CELL.WALL && cell !== CELL.TUNNEL;
}

export function getCellAt(x, z) {
    const col = Math.floor(x);
    const row = Math.floor(z);
    if (row < 0 || row >= ROWS || col < 0 || col >= COLS) return CELL.WALL;
    return MAZE_LAYOUT[row][col];
}

export function setCellAt(x, z, value) {
    const col = Math.floor(x);
    const row = Math.floor(z);
    if (row >= 0 && row < ROWS && col >= 0 && col < COLS) {
        MAZE_LAYOUT[row][col] = value;
    }
}
